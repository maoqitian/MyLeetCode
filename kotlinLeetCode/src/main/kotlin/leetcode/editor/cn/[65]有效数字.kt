class Solution {
    fun isNumber(s: String): Boolean {
        //å°†å­—ç¬¦ä¸²ä»¥ e/E è¿›è¡Œåˆ†å‰²åï¼Œå…¶å®è§„åˆ™ååˆ†ç®€å•ï¼š
        //å¦‚æœå­˜åœ¨ e/E ï¼šå·¦ä¾§å¯ä»¥ã€Œæ•´æ•°ã€æˆ–ã€Œæµ®ç‚¹æ•°ã€ï¼Œå³ä¾§å¿…é¡»æ˜¯ã€Œæ•´æ•°ã€
        //å¦‚æœä¸å­˜åœ¨ e/E ï¼šæ•´æ®µå¯ä»¥æ˜¯ã€Œæ•´æ•°ã€æˆ–ã€Œæµ®ç‚¹æ•°ã€
        //å…³é”®åœ¨äºå¦‚ä½•å®ç°ä¸€ä¸ª check å‡½æ•°ç”¨äºåˆ¤æ–­ã€Œæ•´æ•°ã€æˆ–ã€Œæµ®ç‚¹æ•°ã€ï¼š
        //+/- åªèƒ½å‡ºç°åœ¨å¤´éƒ¨
        //. æœ€å¤šå‡ºç°ä¸€æ¬¡
        //è‡³å°‘å­˜åœ¨ä¸€ä¸ªæ•°å­—
       //æ—¶é—´å¤æ‚åº¦ O(n)
        val n = s.length
        val cs = s.toCharArray()
        var idx = -1
        for (i in 0 until n) {
            if (cs[i] == 'e' || cs[i] == 'E') {
                idx = if (idx == -1) i else return false
            }
        }
        var ans = true
        if (idx != -1) {
            ans = ans and check(cs, 0, idx - 1, false)
            ans = ans and check(cs, idx + 1, n - 1, true)
        } else {
            ans = ans and check(cs, 0, n - 1, false)
        }
        return ans
    }

    private fun check(cs: CharArray, start: Int, end: Int, mustInteger: Boolean): Boolean {
        var start = start
        if (start > end) return false
        if (cs[start] == '+' || cs[start] == '-') start++
        var hasDot = false
        var hasNum = false
        for (i in start..end) {
            if (cs[i] == '.') {
                if (mustInteger || hasDot) return false
                hasDot = true
            } else if (cs[i] in '0'..'9') {
                hasNum = true
            } else {
                return false
            }
        }
        return hasNum
    }
}

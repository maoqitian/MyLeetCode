/**
 * Example:
 * var li = ListNode(5)
 * var v = li.`val`
 * Definition for singly-linked list.
 * class ListNode(var `val`: Int) {
 *     var next: ListNode? = null
 * }
 */
class Solution {
    fun removeElements(head: ListNode?, `val`: Int): ListNode? {

        val res = ListNode(0)
        res.next = head
        //记录当前的节点值
        var temp = res

        while (temp.next != null){

            if (temp.next.`val` == `val`){
                //跳过当前节点
                temp.next = temp.next.next
            }else{
                temp = temp.next
            }
        }

        return res.next

    }
}

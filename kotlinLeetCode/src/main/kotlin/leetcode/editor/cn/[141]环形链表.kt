class Solution {
    fun hasCycle(head: ListNode?): Boolean {
        //æ–¹æ³•ä¸€ hashset æ—¶é—´å¤æ‚åº¦ O(n)
        var tempHead = head
        var hashSet = HashSet<ListNode>()
        while (tempHead != null){
            if (hashSet.contains(tempHead)){
                return true
            }else{
                hashSet.add(tempHead)
            }
            tempHead = tempHead.next
        }
        return false

        //æ–¹æ³•äºŒ å¿«æ…¢åŒæŒ‡é’ˆ éå†
        var tempHead = head
        if (tempHead == null || tempHead.next == null) return false

        var slow = tempHead
        var fast = tempHead.next

        while (slow != fast){
            if (fast == null || fast.next == null){
                //å¿«æ…¢æŒ‡é’ˆ null æ— ç¯
                return false
            }
            //ç§»åŠ¨å¿«æ…¢æŒ‡é’ˆ
            slow = slow?.next
            fast = fast.next.next
        }
        //æœ‰ç¯ è¿­ä»£å®Œæˆ
        return true
    }
}

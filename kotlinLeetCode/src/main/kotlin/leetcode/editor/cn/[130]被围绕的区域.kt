class Solution {
    var n =0
    var m =0
    fun solve(board: Array<CharArray>): Unit {
        n = board.size
        if (n == 0) {
            return
        }
        m = board[0].size
        for (i in 0 until n) {
            dfs(board, i, 0)
            dfs(board, i, m - 1)
        }
        for (i in 1 until m - 1) {
            dfs(board, 0, i)
            dfs(board, n - 1, i)
        }
        for (i in 0 until n) {
            for (j in 0 until m) {
                if (board[i][j] == 'A') {
                    board[i][j] = 'O'
                } else if (board[i][j] == 'O') {
                    board[i][j] = 'X'
                }
            }
        }
    }

    private fun dfs(board: Array<CharArray>, x: Int, y: Int) {
        if (x < 0 || x >= n || y < 0 || y >= m || board[x][y] != 'O') {
            return
        }
        board[x][y] = 'A'
        dfs(board, x + 1, y)
        dfs(board, x - 1, y)
        dfs(board, x, y + 1)
        dfs(board, x, y - 1)
    }
}

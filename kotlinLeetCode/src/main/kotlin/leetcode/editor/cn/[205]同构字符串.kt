class Solution {
    fun isIsomorphic(s: String, t: String): Boolean {

        //HashMap å­˜å‚¨é”®å€¼
        if(s.length != t.length) return false
        val map = HashMap<Char,Char>()
        for (i in 0 until s.length){
            if(!map.containsKey(s[i])){
                if(map.containsValue(t[i])) return false
                //ä¿å­˜é”®å€¼
                map[s[i]] = t[i]
            }else{
                if(map[s[i]] != t[i]){
                    return false
                }
            }
        }
        return true
    }
}

class Solution {
    fun numEquivDominoPairs(dominoes: Array<IntArray>): Int {

        val map = IntArray(100)
        var res = 0
        for (i in dominoes.indices) {
            val m = dominoes[i][0]
            val n = dominoes[i][1]
            val k = if (m > n) m * 10 + n else n * 10 + m
            map[k]++
        }
        for (i in 0..99) {
            res += map[i] * (map[i] - 1) / 2
        }
        return res

    }
}

class Solution {
    fun singleNumber(nums: IntArray): Int {

        var map = HashMap<Int,Int>()

        nums.forEach {
            if (map.containsKey(it)){
                map.put(it,map.get(it)!!+1)
            }else{
                map.put(it,1)
            }
        }

        map.forEach { (t, u) ->
            if (u == 1)  return t
        }
        return -1
        var res = 0

        for (i in 0..31) {
            val mask = 1 shl i
            var cnt = 0
            for (j in 0 until nums.size) {
                if (nums[j] and mask !== 0) {
                    cnt++
                }
            }
            if (cnt % 3 != 0) {
                res = res or mask
            }
        }

        return res
    }
}

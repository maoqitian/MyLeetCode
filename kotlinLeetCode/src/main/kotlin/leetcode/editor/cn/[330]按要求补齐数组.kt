//æ³¨æ„ï¼šæ’å…¥å…ƒç´ çš„èŒƒå›´ä¸º[1,n], è‹¥total<n, åˆ™total+1å±äº[1,n]ä¹‹é—´
// æ—¶é—´å¤æ‚åº¦ O(n)
        var count = 0; //æ’å…¥å…ƒç´ æ•°ç›®
        var total:Long = 0;


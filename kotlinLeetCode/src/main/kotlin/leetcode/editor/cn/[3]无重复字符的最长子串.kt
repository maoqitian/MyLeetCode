        val map = HashMap<Char,Int>()
        var resMax = 0
        //å·¦è¾¹ç•Œ
        var left = 0
        for (index in s.indices){
            if (map.containsKey(s[index])){
                //ç›¸åŒè·³è¿‡ç»§ç»­æ›´æ–°å·¦è¾¹ç•Œ
                left = Math.max(left, map[s[index]]!!+1)

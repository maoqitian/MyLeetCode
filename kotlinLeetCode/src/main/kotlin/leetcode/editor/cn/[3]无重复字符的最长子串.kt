class Solution {
    fun lengthOfLongestSubstring(s: String): Int {

       //æ»‘åŠ¨çª—å£ map ä¿å­˜å­—ç¬¦å’Œä¸‹æ ‡ éå†æ›´æ–°æœ€å¤§é•¿åº¦
